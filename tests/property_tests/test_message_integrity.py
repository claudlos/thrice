# ============================================================================
# Property: consecutive same-role warning
# ============================================================================

@given(n=st.integers(2, 5))
def test_consecutive_user_messages_warned(n):
    """N consecutive user messages produce warnings."""
    msgs = [system_msg()] + [user_msg(f"msg_{i}") for i in range(n)]
    warnings = MessageInvariantChecker.warn_consecutive_same_role(msgs)
    assert len(warnings) == n - 1  # n-1 adjacent pairs


@given(n=st.integers(2, 5))
def test_consecutive_tool_results_not_warned(n):
    """N consecutive tool results are normal (not warned)."""
    call_ids = [f"call_{i}" for i in range(n)]
    msgs = [
        system_msg(),
        user_msg(),
        assistant_msg(tool_calls=call_ids),
    ] + [tool_msg(cid) for cid in call_ids]

    warnings = MessageInvariantChecker.warn_consecutive_same_role(msgs)
    assert len(warnings) == 0
