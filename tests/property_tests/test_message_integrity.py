@given(msgs=well_formed_conversation())
@settings(max_examples=200, deadline=None)
def test_well_formed_conversations_pass_all_checks(msgs):
    """Any well-formed conversation generated by our strategy must pass
    all message invariant safety checks."""
    err = MessageInvariantChecker.check_all(msgs)
    assert err is None, f"Well-formed conversation failed check: {err}"


# ============================================================================
# Property: valid roles are always accepted
# ============================================================================

@given(role=st.sampled_from(VALID_ROLES))
def test_valid_role_accepted(role):
    """Every valid role passes the role check."""
    msgs = [{"role": role, "content": "x"}]
    assert MessageInvariantChecker.check_valid_roles(msgs) is None


# ============================================================================
# Property: invalid roles are always rejected
# ============================================================================

@given(role=st.text(min_size=1, max_size=20).filter(lambda r: r not in VALID_ROLES))
@settings(max_examples=50)
def test_invalid_role_rejected(role):
    """Any role not in VALID_ROLES is caught."""
    msgs = [{"role": role, "content": "x"}]
    err = MessageInvariantChecker.check_valid_roles(msgs)
    assert err is not None
    assert err.invariant_name == "ValidRoles"


# ============================================================================
# Property: tool_call/result pairing
# ============================================================================

@given(n_calls=st.integers(1, 5))
def test_complete_tool_pairing_passes(n_calls):
    """When every tool call has exactly one result, pairing check passes."""
    call_ids = [f"call_{i}" for i in range(n_calls)]
    msgs = [
        system_msg(),
        user_msg(),
        assistant_msg(tool_calls=call_ids),
    ]
    for cid in call_ids:
        msgs.append(tool_msg(cid))

    assert MessageInvariantChecker.check_tool_call_result_pairing(msgs) is None
    assert MessageInvariantChecker.check_no_orphaned_results(msgs) is None


@given(n_calls=st.integers(2, 5), drop_idx=st.data())
def test_missing_tool_result_caught(n_calls, drop_idx):
    """Dropping any one tool result should be caught."""
    call_ids = [f"call_{i}" for i in range(n_calls)]
    idx_to_drop = drop_idx.draw(st.integers(0, n_calls - 1))

    msgs = [
        system_msg(),
        user_msg(),
        assistant_msg(tool_calls=call_ids),
    ]
    for i, cid in enumerate(call_ids):
        if i != idx_to_drop:
            msgs.append(tool_msg(cid))

    err = MessageInvariantChecker.check_tool_call_result_pairing(msgs)
    assert err is not None, f"Missing result for call_{idx_to_drop} not caught"


# ============================================================================
