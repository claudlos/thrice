@given(orphan_id=st.text(min_size=1, max_size=20))
def test_orphaned_result_always_detected(orphan_id):
    """A tool result with no matching call is always detected."""
    msgs = [
        system_msg(),
        user_msg(),
        assistant_msg("No tools used"),
        tool_msg(orphan_id),
    ]
    err = MessageInvariantChecker.check_no_orphaned_results(msgs)
    assert err is not None
    assert err.invariant_name == "NoOrphanedResults"


