"""
Property-based tests for message list integrity.

Properties verified:
  - Message list always valid after any sequence of operations
  - Role alternation: no consecutive same-role (except tool results)
  - Tool call / tool result pairing: every call has exactly one result
  - First message is system or user
  - All roles are valid
  - Tool results appear after their corresponding assistant message
"""

import pytest
from hypothesis import given, settings, HealthCheck, assume
from hypothesis import strategies as st

pytestmark = [pytest.mark.property]


# ============================================================================
# Strategies for generating well-formed and malformed messages
# ============================================================================

VALID_ROLES = ["system", "user", "assistant", "tool"]


def system_msg(content="You are helpful"):
    return {"role": "system", "content": content}


def user_msg(content="Hello"):
    return {"role": "user", "content": content}


def assistant_msg(content="Sure", tool_calls=None):
    msg = {"role": "assistant", "content": content}
    if tool_calls:
        msg["tool_calls"] = [
            {"id": cid, "function": {"name": f"fn_{i}", "arguments": "{}"}}
            for i, cid in enumerate(tool_calls)
        ]
        msg["content"] = None
    return msg


def tool_msg(call_id, content="done"):
    return {"role": "tool", "content": content, "tool_call_id": call_id}


@st.composite
def well_formed_conversation(draw):
    """Generate a well-formed conversation with proper structure."""
    msgs = []

    # Optional system message
    if draw(st.booleans()):
        msgs.append(system_msg(draw(st.text(min_size=1, max_size=50))))

    # User message (required as first non-system)
    msgs.append(user_msg(draw(st.text(min_size=1, max_size=50))))

    # Generate turns
    n_turns = draw(st.integers(0, 5))
    for _ in range(n_turns):
        # Assistant may or may not use tools
        use_tools = draw(st.booleans())
        if use_tools:
            n_calls = draw(st.integers(1, 3))
            call_ids = [f"call_{draw(st.uuids())}" for _ in range(n_calls)]
            msgs.append(assistant_msg(tool_calls=call_ids))
            for cid in call_ids:
                msgs.append(tool_msg(cid, draw(st.text(min_size=1, max_size=30))))
        else:
            msgs.append(assistant_msg(draw(st.text(min_size=1, max_size=50))))

        # Follow up with user message (unless last turn)
        if draw(st.booleans()):
            msgs.append(user_msg(draw(st.text(min_size=1, max_size=50))))

    return msgs


@st.composite
def random_message(draw):
    """Generate a random message (may be malformed)."""
    role = draw(st.sampled_from(VALID_ROLES + ["oracle", "moderator", ""]))
    msg = {"role": role, "content": draw(st.text(max_size=30))}
    if role == "tool":
        msg["tool_call_id"] = draw(st.text(min_size=1, max_size=20))
    if role == "assistant" and draw(st.booleans()):
        n = draw(st.integers(0, 3))
        msg["tool_calls"] = [
            {"id": f"tc_{i}", "function": {"name": "f", "arguments": "{}"}}
            for i in range(n)
        ]
    return msg


