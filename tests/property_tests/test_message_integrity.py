# Property: first message validation
# ============================================================================

@given(first_role=st.sampled_from(["system", "user"]))
def test_valid_first_message_accepted(first_role):
    """system or user as first message is always accepted."""
    msgs = [{"role": first_role, "content": "x"}]
    assert MessageInvariantChecker.check_valid_first_message(msgs) is None


@given(first_role=st.sampled_from(["assistant", "tool"]))
def test_invalid_first_message_rejected(first_role):
    """assistant or tool as first message is always rejected."""
    msg = {"role": first_role, "content": "x"}
    if first_role == "tool":
        msg["tool_call_id"] = "tc_0"
    msgs = [msg]
    err = MessageInvariantChecker.check_valid_first_message(msgs)
    assert err is not None


